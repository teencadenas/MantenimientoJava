    public boolean validarVacio() {

        return txtApellido.getText().equals("") || txtCargo.getText().equals("") || txtEmailEmpresa.getText().equals("")
                || txtEmailPersona.getText().equals("") || txtEstado.getText().equals("") || txtIdentificacion.getText().equals("")
                || txtNombre.getText().equals("") || txtTelefono.getText().equals("");

    }

    //Se restablece el formulario
    public void limpiarFormulario() {

        //Se limpian casillas
        txtApellido.setText("");
        txtCargo.setText("");
        txtEmailEmpresa.setText("");
        txtEmailPersona.setText("");
        txtEstado.setText("");
        txtIdentificacion.setText("");
        txtNombre.setText("");
        txtTelefono.setText("");
        //Se habilitan/deshabiliatan casillas
        txtApellido.setEnabled(true);
        txtCargo.setEnabled(true);
        txtEmailEmpresa.setEnabled(true);
        txtEmailPersona.setEnabled(true);
        txtEstado.setEnabled(true);
        txtIdentificacion.setEnabled(true);
        txtNombre.setEnabled(true);
        txtTelefono.setEnabled(true);
        //Se habiltan/deshabilitan botones
        btnBuscar.setEnabled(true);
        btnEliminar.setEnabled(false);
        btnNuevo.setEnabled(true);
        btnActualizar.setEnabled(false);

    }

    //Alerta de confirmacion
    public int alertaConsulta(String accion) {

        return JOptionPane.showInternalConfirmDialog(null, "Se :" + accion
                + "Identificacion :" + txtIdentificacion.getText() + "\n"
                + " Nombre :" + txtNombre.getText() + "\n"
                + " Apellido :" + txtApellido.getText() + "\n"
                + " Telefono :" + txtTelefono.getText() + "\n"
                + " Cargo :" + txtCargo.getText() + "\n"
                + " Estado :" + txtEstado.getText() + "\n"
                + " E-mail Per.:" + txtEmailPersona.getText() + "\n"
                + " E-Mail Emp.: " + txtEmailEmpresa.getText(),
                "Confirmacion", JOptionPane.OK_CANCEL_OPTION, JOptionPane.INFORMATION_MESSAGE);

    }

    //Funcion buscar registro
    public boolean buscarRegistro() {
        try {

            //Se realiza consulta
            e = ce.buscarEmpleado(txtIdentificacion.getText());

            //Decision segun respuesta
            if (e != null) {

                //Se asignan valores a las casillas del formulario
                txtIdentificacion.setText(e.getIdentificacionPersona());
                txtNombre.setText(e.getNombrePersona());
                txtApellido.setText(e.getApellido());
                txtTelefono.setText(e.getTelefonoPersona());
                txtEmailPersona.setText(e.getMailPersona());
                txtEmailEmpresa.setText(e.getMailEmpresa());
                txtCargo.setText(e.getCargo());
                txtEstado.setText(e.getEstado());
                return true;
            }
        } catch (Exception ex) {

            Logger.getLogger(EmpleadoFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }

    //Funcion eliminar registro
    public void eliminarEmpleado() {
        try {

            //Se realiza consulta
            ce.eliminarEmpleado(txtIdentificacion.getText());

        } catch (Exception ex) {

            Logger.getLogger(EmpleadoFrm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    //Funcion actualizar empleado
    private void actualizarEmpleado() {
        try {

            //Se crea la clase para la consulta
            e = new Empleado("", txtCargo.getText(), txtEmailEmpresa.getText(), txtEstado.getText(),
                    txtNombre.getText(), txtApellido.getText(), txtTelefono.getText(), txtEmailPersona.getText(),
                    txtIdentificacion.getText());

            //Se realiza consulta
            ce.actualizarEmpleado(e);
        } catch (Exception ex) {

            Logger.getLogger(EmpleadoFrm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
