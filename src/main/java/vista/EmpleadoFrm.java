    //Accion click crear nuevo registro
    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

        //VAlidar casillas vacias
        if (validarVacio()) {
            limpiarFormulario();
            JOptionPane.showMessageDialog(null, "Complete el formulario");
        } else {

            try {

                //Se crea la clase para la consulta
                e = new Empleado("", txtCargo.getText(), txtEmailEmpresa.getText(), txtEstado.getText(),
                        txtNombre.getText(), txtApellido.getText(), txtTelefono.getText(), txtEmailPersona.getText(),
                        txtIdentificacion.getText());

                //Se realiza consulta
                ce.nuevoEmpleado(e);
                
                JOptionPane.showMessageDialog(null, "Se creo registro");
            } catch (HeadlessException ex) {

                Logger.getLogger(EmpleadoFrm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_btnNuevoActionPerformed

    //Funcion buscar registro
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        //Condicional de casillas vacias
        if (txtIdentificacion.getText().equals("")) {
            
            JOptionPane.showMessageDialog(null, "Llene todos los valores del Formulario");
        } else {

            buscarRegistro();
            if (!buscarRegistro()) {
                
                JOptionPane.showMessageDialog(null, "Registro no encontrado");
                
            } else {

                //Configuraciones de seguridad
                txtIdentificacion.setEnabled(false);
                btnActualizar.setEnabled(true);
                btnEliminar.setEnabled(true);
                btnNuevo.setEnabled(false);

            }
        }
        limpiarFormulario();
    }//GEN-LAST:event_btnBuscarActionPerformed

    //Accion click boton eliminar
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        //Verificacion casillas vacias
        if (validarVacio()) {

            JOptionPane.showMessageDialog(null, "LLeno todos los valores del formulario");
        } else {

            //Mensaje para confirmacion
            int confirmacion = alertaConsulta("eliminara");

            //Decision frente a confirmacion
            switch (confirmacion) {
                case 0 -> {

                    //Se realiza consulta
                    eliminarEmpleado();
                    JOptionPane.showMessageDialog(null, "Se elimino el registro");
                }
                case 1 -> {
                    break;
                }
                default ->
                    throw new AssertionError();
            }
        }
        limpiarFormulario();
    }//GEN-LAST:event_btnEliminarActionPerformed

    //Accion click boton actualizar
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        //Verificacion vacios
        if (validarVacio()) {

            JOptionPane.showMessageDialog(null, "LLene todos los valores del formulario");
        } else {

            //Alerta de confirmacion
            int confirmacion = alertaConsulta("actualizara");

            //Decision segun confirmacion
            switch (confirmacion) {
                case 0 -> {

                    //Se realiza consulta
                    actualizarEmpleado();
                    JOptionPane.showMessageDialog(null, "Se actualizo el registro");
                }
                case 1 -> {
                    break;
                }
                default ->
                    throw new AssertionError();
            }
        }
        limpiarFormulario();
    }//GEN-LAST:event_btnActualizarActionPerformed

